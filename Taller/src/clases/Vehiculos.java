public class Vehiculos {
private String matricula;
private String marca;
private String modelo;
private String tipo;



public Vehiculos() {
}


public Vehiculos(String matricula, String marca, String modelo, String tipo) {
    this.matricula = matricula;
    this.marca = marca;
    this.modelo = modelo;
    this.tipo = tipo;
}

   

   
}
